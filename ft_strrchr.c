#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p;

	p = NULL;
	while (*s)
	{
		if (*s == (char)c)
			p = (char*)s;
		s++;
	}
	if (*s == '\0' && c == '\0')
		p = (char*)s;
	return (p);
}
