#include "libft.h"
#include <stdio.h>

void	*ft_memset(void *str, int x, size_t len)
{
	char	*p;
	int		i;

	p = str;
	i = 0;
	while (len--)
	{
		p[i] = x;
		i++;
	}
	return (str);
}
