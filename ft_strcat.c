#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	char	*str;

	str = dst;
	while (*dst)
		dst++;
	while (*src)
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (str);
}
